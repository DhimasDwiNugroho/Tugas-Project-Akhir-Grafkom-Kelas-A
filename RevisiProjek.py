def input_mouse(button, state, x, y):
    global hijau, kuning, merah
    # Saat mengklik kanan warna kotak akan berubah menjadi warna  merah
    if button == GLUT_RIGHT_BUTTON and state == GLUT_DOWN:
        if merah< 1:
            hijau = 0
            kuning = 1
            merah = 0
        else:
            hijau = 0
            kuning = 0
            merah = 1
            print("Klik kanan ditekan ", "(", x, ",", y, ")")
    # Saat mengklik kiri warna kotak akan berubah menjadi warna hijau 
    elif button == GLUT_LEFT_BUTTON and state == GLUT_DOWN:
        if hijau < 1:
            hijau = 1
            kuning = 0
            merah = 0
        elif merah < 1:
            hijau = 0
            kuning = 0
            merah = 0
            print("Klik Kiri ditekan ", "(", x, ",", y, ")")

def input_keyboard(key,x,y):
    global pos_x_pemain, pos_y_pemain, gerak
    # Untuk mengubah posisi kotak
    gerak = 10
    if key == GLUT_KEY_UP:
        pos_y_pemain += gerak
        # print("Tombol Atas ditekan ", "x : ", pos_x, " y : ", pos_y)
    elif key == GLUT_KEY_DOWN:
        pos_y_pemain -= gerak
        # print("Tombol Bawah ditekan ", "x : ", pos_x, " y : ", pos_y)
    elif key == GLUT_KEY_RIGHT:
        pos_x_pemain += gerak
        # print("Tombol Kanan ditekan ", "x : ", pos_x, " y : ", pos_y)
    elif key == GLUT_KEY_LEFT:
        pos_x_pemain -= gerak
        # print("Tombol Kiri ditekan ", "x : ", pos_x, " y : ", pos_y)

